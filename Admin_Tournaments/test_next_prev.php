$(document).ready(function() {

    // Collect unique column groups (H_Column_9 â†’ H_Column_16)
    var columnGroups = [...new Set($('#myTable td[class^="H_Column_"], #myTable th[class^="H_Column_"]')
        .map(function() { return $(this).attr('class'); })
        .get())];

    var showCount = 2;            // show 2 groups at a time
    var currentIndex = 0;         // starting at group 0 (H_Column_9)

    function updateColumns() {
        // Hide all
        columnGroups.forEach(function(group) {
            $('.' + group).hide();
        });

        // Show 2 visible groups
        $('.' + columnGroups[currentIndex]).show();
        $('.' + columnGroups[currentIndex + 1]).show();
    }

    updateColumns(); // initial state

    $('#nextColumn').click(function() {
        if (currentIndex < columnGroups.length - showCount) {
            currentIndex++;
            updateColumns();
        }
    });

    $('#prevColumn').click(function() {
        if (currentIndex > 0) {
            currentIndex--;
            updateColumns();
        }
    });

});


</script>